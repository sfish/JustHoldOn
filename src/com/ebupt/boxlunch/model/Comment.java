package com.ebupt.boxlunch.model;

public class Comment {
	//private String pNum = null;
	
	private String uid =null;//用户的唯一标识符
	private String username =null;//用户名，相当于昵称

	private String userpic  = null;//头像地址
	private String commentid  = null;//评论id
	private String comment  = null;//评论内容
	private String commenttime  = null;//评论时间
	
	public Comment(){
	
	}
	
	public String getUid() {
		return uid;
	}

	public void setUid(String pNum) {
		this.uid = pNum;
	}


	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	

	public String getUserpic() {
		return userpic;
	}

	public void setUserpic(String userpic) {
		this.userpic = userpic;
	}

	public String getCommentid() {
		return commentid;
	}

	public void setCommentid(String commentid) {
		this.commentid = commentid;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getCommenttime() {
		return commenttime;
	}

	public void setCommenttime(String commenttime) {
		this.commenttime = commenttime;
	}
}
